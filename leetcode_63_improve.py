class Solution(object):
    def uniquePathsWithObstacles(self, obstacleGrid):
        """
        :type obstacleGrid: List[List[int]]
        :rtype: int
        """
        result = []
        for index1, x in enumerate(obstacleGrid):
            temp = []
            for index2, y in enumerate(x):
                # 第一个点
                if index2 == 0 and index1 == 0:
                    if y == 0:
                        temp.append(1)
                    else:
                        temp.append(0)
                    continue

                # 第一行
                if index1 == 0:
                    if y == 0:
                        temp.append(temp[-1])
                    else:
                        temp.append(0)
                    continue
                # 第一列
                if index2 == 0:
                    if y == 0:
                        temp.append(result[-1][0])
                    else:
                        temp.append(0)
                    continue
                # 正常
                if y == 0:
                    temp.append(temp[-1] + result[-1][index2])
                else:
                    temp.append(0)
            result.append(temp)
        return result[-1][-1]


